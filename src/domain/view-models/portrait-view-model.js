const TEN_GOD_SET = new Set(Object.keys(TEN_GOD));

// 统计八字主导十神：年/月/时柱天干十神 + 四柱藏干十神，频次最高者（日柱天干为日主本身，跳过）。
const dominantTenGod = (bazi) => {
  const pillars = bazi?.pillars || [];
  const counts = {};
  pillars.forEach((pillar) => {
    if (pillar.label !== '日柱' && TEN_GOD_SET.has(pillar.ganShiShen)) {
      counts[pillar.ganShiShen] = (counts[pillar.ganShiShen] || 0) + 1.5; // 透干权重高
    }
    (pillar.hiddenShiShen || []).forEach((god) => {
      if (TEN_GOD_SET.has(god)) counts[god] = (counts[god] || 0) + 1;
    });
  });
  let top = '';
  let max = 0;
  Object.entries(counts).forEach(([god, n]) => {
    if (n > max) { max = n; top = god; }
  });
  return top;
};

const mainStarOfMing = (palaces) => {
  const ming = (palaces || []).find((palace) => palace.name === '命宫');
  const stars = (ming?.majorStars || []).map((star) => star.name).filter(Boolean);
  const named = stars.find((name) => MAIN_STAR[name]);
  if (named) return { star: named, line: MAIN_STAR[named] };
  if (!stars.length) return { star: '空宫', line: MAIN_STAR['空宫'] };
  return { star: stars[0], line: '' };
};

export const createPortraitViewModel = (state) => {
  const data = state.astrolabeData;
  if (!data) return { ready: false };

  const five = data.reading?.fiveElement;
  const stem = data.bazi?.dayMaster?.stem;
  const element = data.bazi?.dayMaster?.element;
  const lead = five?.favored?.[0];
  if (!stem || !STEM_IMAGE[stem] || !lead || !TENSION[lead]) {
    return { ready: false };
  }

  // 拍 1：底色 + 张力（hook）。张力句单独留一份，分享卡用。
  const tension = TENSION[lead];
  const hook = `你是${stem}${element || ''}——${STEM_IMAGE[stem]}。${tension}`;

  // 拍 2 / 3 / 4：命宫主星、主导十神、格局
  const paragraphs = [];
  const ming = mainStarOfMing(data.palaces);
  if (ming.line) paragraphs.push(ming.line);

  const god = dominantTenGod(data.bazi);
  if (god) paragraphs.push(`再往里看，你八字里"${god}"的劲最重——${TEN_GOD[god]}`);

  const patternName = data.reading?.patterns?.[0]?.name;
  if (patternName) {
    paragraphs.push(PATTERN_FLAVOR[patternName]
      || `你命里还落了个"${patternName}"，是这张盘上比较出挑的一笔。`);
  }

  // 拍 5：软转（turn）
  const turn = TURN[lead];

  // 分享卡：浓缩成"命格标签 + 一句最戳的张力 + 软转"，适合截图发出去。
  // 张力句去掉承接转折的开头"可"，让它单独站住也读得顺。
  const shareTension = tension.replace(/^可/, '');
  const tags = [`${stem}${element || ''}`, `${ming.star}坐命`];
  if (god) tags.push(`${god}驱动`);
  const tagline = tags.join(' · ');
  const shareText = [
    `我的命格 · ${tagline}`,
    shareTension,
    turn,
    '—— MINGLI 命理',
  ].join('\n');

  return {
    ready: true,
    kicker: '先别急着看题——先看看你这个人',
    title: '你这张盘，大概是这样一个人',
    hook,
    paragraphs,
    turn,
    note: '这段是按你的八字、命宫主星与调候推出来的倾向，说的是你大概率有的体感，不是命定。',
    shareCard: { tagline, tension: shareTension, turn },
    shareText,
  };
};
